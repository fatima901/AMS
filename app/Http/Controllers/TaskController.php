    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Task  $task
     * @return \Illuminate\Http\Response
     */
    public function com(Task $id)
    {
        $id->status = 1;
        $id->save();
        return back()->with('success', 'Task Completed Sucessfully');;
    }

    public function del(Task $id)
    {
        $id->delete();
        return back()->with('error', 'Task Deleted Sucessfully');;
    }
